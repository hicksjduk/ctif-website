<h1>Recycling</h1>
<h2><a id="foil">Aluminium foil</a></h2>
<p>The churches work together to collect foil, which is taken to the Queen Elizabeth II
Activity Centre in Manor Farm Country Park - they sell the foil for recycling, and raise money
for equipment. Foil is collected in various churches, and there is a publicly-available bin
at Immaculate Conception Roman Catholic
church in Stubbington. <b>Please note that the bins at the Palmerston Avenue car park in central Fareham
are now closed.</b> 
<p><b>Please</b> make sure that the foil is clean, and is actually foil not shiny plastic!
If in doubt, use the 'scrunch' test - scrunch it in your hand and release it, if it stays scrunched
it is probably aluminium, if it doesn't it definitely isn't.
<h2>Other things</h2>
<p>Various churches in Fareham provide recycling facilities for items that are not
recycled by the council. Below are some details of which we have been notified.
<h3>Fareham United Reformed Church</h3>
<ul>
<li>Plastic milk bottle tops (only milk bottle tops and please make sure they're clean), 
raise money for Hampshire East Girlguiding
<li>Printer cartridges, taken to a local infant school
<li>Aluminium foil (see above)
<li>Toothbrushes, toothpaste, make-up tubes etc, taken to Eco-freaks in Lee on Solent.
<li>Pens - ballpoints, gel pens, fountain pens, mechanical pencils, refills & cartridges, 
correction instruments (bottles, tapes and pens), highlighters, felt tips, whiteboard and 
permanent markers - no colouring pencils. 
These will then be taken to the Methodist Church as part of the Terracycle project.
<li>Used stamps for RNIB 
</ul>
<h3>St John's</h3>
<p>Used stamps, coins, spectacles, hearing aids, mobile phones, ink cartridges, aluminum foil,
milk bottle tops. See <a href="https://stjohnsfareham.org.uk/ecochurch/">the church's
recycling page</a> for more details.
<h3>Fareham Methodist Church - Terracycle</h3>
<p>There is a blue Terracycle bin outside the church, in which a variety of items can be placed.
For details of what is accepted, please <a href="Terracycle.pdf">see here</a>. Note that
although the individual programmes are sponsored by particular brands, all brands of the
relevant packaging type are accepted.
