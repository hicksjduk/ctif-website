<h1>Holiday Club 2023</h1>
<img src="/images/holclub2023/Welcome.jpg" width="200" align="left" style="padding-right: 20px">
Our ecumenical summer holiday club, for children in school years 1-6, ran from Monday 24th
until Friday 28th July.
58 children attended, and almost as many volunteers, drawn from many different churches,
helped to care for the children in their groups, run activities and lead sessions of
celebration and teaching at the start and end of each day.
<p>
The theme was <b>Bright Sparks</b>, and focused on Jesus, the Light of the World, and how
he brings light into our lives. Our memory verse was taken from John 8:12 - 
<i>I am the light of the world. Whoever follows me will not walk in darkness, but will
have the light of life.</i>
<p>We experienced so many blessings. The children had a great time, loved the songs,
engaged with the activities, and established fantastic relationships with each other and
with the adult volunteers. Parents - many of whom had little or no link to any church -
told us how much they and their children appreciated the week.
